# operadores Aritmeticos
# Para realizar operaciones se utiliza el pemdas (Parentesis, Exponentes, Multiplicacion y Division, Adicion y Sustraccion)
# En Python no se pueden hacer divisiones entre 0

a=25
b=35

print("potenciacion:", pow(a,b))
print("exponente:", a**b)
print("multiplicacion:", a*b)
print("division:", a/b)
print("division entera:", a//b)
print("modulo:", a%b)
print("suma:", a+b)
print("resta:", a-b)
